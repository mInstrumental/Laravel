                            <li>Tu consentimiento explícito y verificable</li>
                            <li>Cumplimiento de obligaciones legales</li>
                            <li>Ejecución de un contrato</li>
                            <li>Interés legítimo cuando no prevalezcan tus derechos y libertades</li>
                        </ul>

